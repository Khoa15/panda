    private void txtFieldProfileKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFieldProfileKeyReleased
        // TODO add your handling code here:
        boolean flag = true;
        for(String profile : profiles){
            if(profile.equals(txtFieldProfile.getText())){
                flag = false;
            }
        }
        cbInsert.setSelected(flag);
    }//GEN-LAST:event_txtFieldProfileKeyReleased

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        String profile = txtFieldProfile.getText();
        if(profile.isEmpty()){
            JOptionPane.showConfirmDialog(this, "Vui lòng chọn profile", "Thông báo!", JOptionPane.DEFAULT_OPTION);
            return;
        }
        
        if(SystemDAO.deleteProfile(profile)){
            JOptionPane.showConfirmDialog(this, "Successfully!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
            init();
        }else{
            JOptionPane.showConfirmDialog(this, "Failure!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
        }
        
    }//GEN-LAST:event_btnDeleteMouseClicked
    private void setData(){
        txtFieldProfile.setText(currentProfile.profile);
        txtFieldTitle.setText(currentProfile.resource_name);
        txtFieldLimit.setText(currentProfile.limit);
    }
