    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        Profile profile = new Profile(
            currentProfile.profile,
            txtFieldTitle.getText(),
            txtFieldLimit.getText()
        );
        if(cbInsert.isSelected()){
            profile.profile = txtFieldProfile.getText();
            try{
                if(SystemDAO.insertProfile(profile)){
                    JOptionPane.showConfirmDialog(this, "Successfully!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
                    profiles.add(profile.profile);
                    init();
                }else{
                    JOptionPane.showConfirmDialog(this, "Failure!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
                }
            }catch(Exception e){
                JOptionPane.showConfirmDialog(this, e.getMessage(), "Thông báo!", JOptionPane.DEFAULT_OPTION);
            }
        }else{
            if(SystemDAO.updateProfile(profile)){
                JOptionPane.showConfirmDialog(this, "Successfully!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
                init();
            }else{
                JOptionPane.showConfirmDialog(this, "Failure!", "Thông báo!", JOptionPane.DEFAULT_OPTION);
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void tbListProfilesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListProfilesMouseClicked
        // TODO add your handling code here:
        int row = tbListProfiles.getSelectedRow();
        currentProfile.profile = tbListProfiles.getValueAt(row, 0).toString();
        currentProfile.resource_name = tbListProfiles.getValueAt(row, 1).toString();
        currentProfile.limit = tbListProfiles.getValueAt(row, 3).toString();
        
        setData();
    }//GEN-LAST:event_tbListProfilesMouseClicked

