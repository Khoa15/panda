    private void txtFieldTimeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFieldTimeKeyPressed
        // TODO add your handling code here:
        //38 Arrow Up
        //40 Arrow Down
        int i = 1;
        switch(evt.getKeyCode()){
            case 38:
                i = 1;
                break;
            case 40:
                i = -1;
                break;
            default:
                evt.consume();
                break;
        }
        second+= i;
        if(second >= 60 && evt.getKeyCode() == 38){
            minute++;
            second = 0;
            if(minute >= 60){
                minute = 0;
                hour++;
                if(hour >= 24){
                    hour = 0;
                }
            }
        }else if(second <= 0 && evt.getKeyCode() == 40){
            second = 59;
            minute--;
            if(minute <= 0){
                minute = 59;
                hour--;
                if(hour <= 0){
                    hour = 23;
                }
            }
        }
        initTime();
    }//GEN-LAST:event_txtFieldTimeKeyPressed


